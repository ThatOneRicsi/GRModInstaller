    public static InstallerLanguage Resolve(CultureInfo culture)
    {
        var name = culture.Name;

        if (TryFind(name, out var exact))
        {
            return exact;
        }

        if (name.StartsWith("zh", StringComparison.OrdinalIgnoreCase))
        {
            return name.Contains("TW", StringComparison.OrdinalIgnoreCase) ||
                   name.Contains("HK", StringComparison.OrdinalIgnoreCase) ||
                   name.Contains("MO", StringComparison.OrdinalIgnoreCase)
                ? Find("zh-Hant")
                : Find("zh-Hans");
        }

        if (name.StartsWith("pt", StringComparison.OrdinalIgnoreCase))
        {
            return name.Contains("BR", StringComparison.OrdinalIgnoreCase) ? Find("pt-BR") : Find("pt-PT");
        }

        if (name.StartsWith("no", StringComparison.OrdinalIgnoreCase) ||
            name.StartsWith("nb", StringComparison.OrdinalIgnoreCase) ||
            name.StartsWith("nn", StringComparison.OrdinalIgnoreCase))
        {
            return Find("nb");
        }

        if (TryFind(culture.TwoLetterISOLanguageName, out var neutral))
        {
            return neutral;
        }

        return English;
    }

    public static CultureInfo GetCulture(InstallerLanguage language)
    {
        return CultureInfo.GetCultureInfo(language.Code);
    }

    public static string Get(InstallerLanguage language, TextId id)
    {
        if (language.Texts.TryGetValue(id, out var value))
        {
            return value;
        }

        return English.Texts[id];
    }

    private static InstallerLanguage CreateLanguage(string code, string displayName, bool isRightToLeft, params (TextId Id, string Value)[] overrides)
    {
        var texts = English.Texts.ToDictionary(entry => entry.Key, entry => entry.Value);

        foreach (var (id, value) in overrides)
        {
            texts[id] = value;
        }

        return new InstallerLanguage(code, displayName, isRightToLeft, texts);
    }

    private static InstallerLanguage Find(string code)
    {
        return SupportedLanguages.First(language => string.Equals(language.Code, code, StringComparison.OrdinalIgnoreCase));
    }

    private static bool TryFind(string code, out InstallerLanguage language)
    {
        language = SupportedLanguages.FirstOrDefault(item => string.Equals(item.Code, code, StringComparison.OrdinalIgnoreCase)) ?? English;
        return !ReferenceEquals(language, English) || string.Equals(code, English.Code, StringComparison.OrdinalIgnoreCase);
    }
